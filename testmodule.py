#Function to evaluate choice.
def choice_eval(choice):

    #If choice is "Banana," print "Banana."
    if choice == "Banana":
        print("Banana")

    #If choice is "Apple," print "Apple."
    elif choice == "Apple":
        print("Apple")

    #If choice is neither "Banana" nor "Apple," print "Boring."
    else:
        print("Boring")